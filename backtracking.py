# Backtracking Pattern

# Used to solve problems that involve exploring all potential solution paths and backtracking
# the paths that don't lead to a valid solution.

# For example:
# - generate all possible permutation/combinations of a set of numbers
# - solve puzzles like Sudoku, N-Queens, and word search
# - Find all possible paths from start to end in a graph or a maze
# - Generate all valid parentheses of a given length

