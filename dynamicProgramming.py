# Dynamic Programming Pattern

# Solve optimization problems by breaking them down into smaller sub-problems
# and storing their solutions to avoid repeptitive work.
# Useful for problems with overlapping subproblems and optimal substructure.
# Examples include:
# - Maximize or minimize a certain value (e.g., profit, cost, distance)
# - Count the number of ways to do something (e.g., combinations, permutations)

# Some common dynamic programming problems include:
# - Fibonacci sequence
# - 0/1 Knapsack problem
# - Longest Common Subsequence (LCS)
# - Longest Increasing Subsequence (LIS)
# - Subset Sum
# - Matrix Chain Multiplication

